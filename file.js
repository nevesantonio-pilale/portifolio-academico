
function abrirMenu() {
	
	document.getElementById('menu').style.display = "block";
	document.getElementById('img-menu').style.display = "none";
	
	document.getElementById('img-close').style.display = "block";
}


function closerMenu() {
	
	document.getElementById('menu').style.display = "none";
	document.getElementById('img-menu').style.display = "block";
	
	document.getElementById('img-close').style.display = "none";
}


function fecharMenu() {
	
	document.getElementById('menu').style.display = "none";
	document.getElementById('img-close').style.display = "none";
	document.getElementById('img-menu').style.display = "block";
}

